// Primitive Data Types

// String
let name = "Robin";

// Number
let age = 25;

// Boolean
let isFrontendDeveloper = true;

// Undefined
let city;

// Null
let salary = null;

// Print Values
console.log(name);
console.log(age);
console.log(isFrontendDeveloper);
console.log(city);
console.log(salary);

// Check Data Types
console.log(typeof name);
console.log(typeof age);
console.log(typeof isFrontendDeveloper);
console.log(typeof city);
console.log(typeof salary); // object (JavaScript historical behavior)